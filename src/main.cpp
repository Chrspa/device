void beginSend() {
    char str = 'a';
    SERIAL1.write(str);
    while (1) {
        if (SERIAL1.available() > 0) {
            delay(10);
            char str1 = SERIAL1.read();
            Serial.print(str1);
            if (str1 == 'b') {
                break;
            }
        }
    }
}

